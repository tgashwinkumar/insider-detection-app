export function getMockMarketData(conditionId) {
  const market = MOCK_MARKETS.find((m) => m.conditionId === conditionId) || MOCK_MARKETS[0]
  return {
    market,
    trades: MOCK_TRADES,
    verdict: {
      level: market.verdict,
      direction: market.direction,
      confidence: market.confidence,
    },
    summary: {
      totalTrades: MOCK_TRADES.length,
      uniqueWallets: new Set(MOCK_TRADES.map((t) => t.wallet)).size,
      flaggedCount: MOCK_TRADES.filter((t) => t.classification === 'insider').length,
      highestScore: Math.max(...MOCK_TRADES.map((t) => t.insiderScore)),
    },
  }
}

